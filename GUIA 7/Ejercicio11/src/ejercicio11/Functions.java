public class Functions {
    public static int sumar(int n1, int n2){
        int suma;
        suma=n1+n2;        
        return suma;
    }
    
        public static int restar(int n1, int n2){
        int resta;
        resta=n1-n2;        
        return resta;
    }
        
    public static int multiplicar(int n1, int n2) {
        int producto;
        producto = n1 - n2;
        return producto;
    }
    
    public static int dividir(int n1, int n2) {
        int division;
        division = n1 / n2;
        return division;
    }
        
        
}
